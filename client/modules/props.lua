local attachedProps = {}

RegisterNetEvent('core:attachItem')
AddEventHandler('core:attachItem', function(item)
	if attachedProps[item] then TriggerEvent('core:consoleLog', self, 'props', 'attempted overriding prop value for ' .. item) return end
	local hash = GetHashKey(propList[item]['model'])
	core.utils.loadModel(hash)
	local prop = CreateObject(hash, 1.0, 1.0, 1.0, 1, 1, 0)
	core.utils.setGlobalObject(prop)
	AttachEntityToEntity(prop, PlayerPedId(), GetPedBoneIndex(PlayerPedId(), propList[item]['bone']), propList[item]['x'], propList[item]['y'], propList[item]['z'], propList[item]['xR'], propList[item]['yR'], propList[item]['zR'], 1, 1, 0, 0, 2, 1)
	attachedProps[item] = prop
end)

RegisterNetEvent('core:removeProp')
AddEventHandler('core:removeProp', function(item)
	if not DoesEntityExist(attachedProps[item]) then TriggerEvent('core:consoleLog', self, 'props', 'tried removing non-existing prop: ' .. item) return end
	DeleteEntity(attachedProps[item])
	attachedProps[item] = nil
end)

RegisterNetEvent('core:removeAllProps')
AddEventHandler('core:removeAllProps', function()
	for k,v in pairs(attachedProps) do
		TriggerEvent('core:removeProp', k)
	end
end)